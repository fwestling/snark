hdl64::db zerodb()
{
    velodyne::hdl64::db db;
    for( unsigned int i = 0; i < db.lasers.size(); ++i )
    {
        db.lasers[i].horizontal_offset = 0;
        db.lasers[i].vertical_offset = 0;
        db.lasers[i].distance_correction = 0;
    }
    return db;
}

hdl64::db testdb()
{
    velodyne::hdl64::db db;
    std::istringstream iss( db_string );
    assert( iss.good() );
    iss >> db;
    return db;    
}

